// FlowRowLayout utility to support layout constraints gracefully in Compose
@Composable
fun FlowRowLayout(
    spacing: androidx.compose.ui.unit.Dp = 6.dp,
    modifier: Modifier = Modifier,
    content: @Composable () -> Unit
) {
    androidx.compose.ui.layout.Layout(
        content = content,
        modifier = modifier
    ) { measurables, constraints ->
        val xSpacing = spacing.roundToPx()
        val ySpacing = spacing.roundToPx()

        val placeables = measurables.map { it.measure(constraints.copy(minWidth = 0, minHeight = 0)) }
        val layoutRows = mutableListOf<List<androidx.compose.ui.layout.Placeable>>()
        val rowHeights = mutableListOf<Int>()

        var currentRow = mutableListOf<androidx.compose.ui.layout.Placeable>()
        var currentRowWidth = 0
        var currentRowHeight = 0

        placeables.forEach { placeable ->
            if (currentRowWidth + placeable.width > constraints.maxWidth) {
                // Wrap to next row
                layoutRows.add(currentRow)
                rowHeights.add(currentRowHeight)
                currentRow = mutableListOf(placeable)
                currentRowWidth = placeable.width + xSpacing
                currentRowHeight = placeable.height
            } else {
                currentRow.add(placeable)
                currentRowWidth += placeable.width + xSpacing
                currentRowHeight = maxOf(currentRowHeight, placeable.height)
            }
        }
        if (currentRow.isNotEmpty()) {
            layoutRows.add(currentRow)
            rowHeights.add(currentRowHeight)
        }

        val totalHeight = rowHeights.sum() + (rowHeights.size - 1).coerceAtLeast(0) * ySpacing
        layout(constraints.maxWidth, totalHeight) {
            var currentY = 0
            layoutRows.forEachIndexed { rowIndex, row ->
                var currentX = 0
                val rowHeight = rowHeights[rowIndex]
                row.forEach { placeable ->
                    placeable.placeRelative(currentX, currentY + (rowHeight - placeable.height) / 2)
                    currentX += placeable.width + xSpacing
                }
                currentY += rowHeight + ySpacing
            }
        }
    }
}
