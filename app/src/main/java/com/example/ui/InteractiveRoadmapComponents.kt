                    text = "Projected Study Time Horizon Graph",
                    style = MaterialTheme.typography.labelSmall,
                    fontWeight = FontWeight.Bold,
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.6f)
                )

                Spacer(modifier = Modifier.height(8.dp))

                // Chart Graphics showing 3 segments of roadmap progression and weeks required
                val theoryWeeks = (weeksRequiredProjected * 0.4f).coerceAtLeast(1f)
                val practiceWeeks = (weeksRequiredProjected * 0.4f).coerceAtLeast(1f)
                val testWeeks = (weeksRequiredProjected * 0.2f).coerceAtLeast(1f)
                val totalProportion = theoryWeeks + practiceWeeks + testWeeks

                Row(
                    modifier = Modifier
                        .fillMaxWidth()
                        .height(36.dp)
                        .clip(RoundedCornerShape(8.dp))
                ) {
                    Box(
                        modifier = Modifier
                            .fillMaxHeight()
                            .weight(theoryWeeks / totalProportion)
                            .background(Brush.horizontalGradient(listOf(PrimaryPurple, PrimaryPurple.copy(alpha = 0.8f)))),
                        contentAlignment = Alignment.Center
                    ) {
                        Text(
                            text = "Theory: %.1fw".format(theoryWeeks),
                            style = MaterialTheme.typography.labelSmall,
                            fontWeight = FontWeight.Bold,
                            color = Color.White,
                            fontSize = 9.sp,
                            maxLines = 1
                        )
                    }
                    Box(
                        modifier = Modifier
                            .fillMaxHeight()
                            .weight(practiceWeeks / totalProportion)
                            .background(Brush.horizontalGradient(listOf(SecondaryPink, SecondaryPink.copy(alpha = 0.8f)))),
                        contentAlignment = Alignment.Center
                    ) {
                        Text(
                            text = "Projects: %.1fw".format(practiceWeeks),
                            style = MaterialTheme.typography.labelSmall,
                            fontWeight = FontWeight.Bold,
                            color = Color.White,
                            fontSize = 9.sp,
                            maxLines = 1
                        )
                    }
                    Box(
                        modifier = Modifier
