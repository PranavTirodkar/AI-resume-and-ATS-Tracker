                Divider(
                    color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.05f),
                    thickness = 0.8.dp,
                    modifier = Modifier.padding(vertical = 10.dp)
                )

                // STACKED TIME HORIZON BAR CHART GRAPH
