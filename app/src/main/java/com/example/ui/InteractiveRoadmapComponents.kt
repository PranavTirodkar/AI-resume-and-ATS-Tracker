@Composable
fun TimelineAndPlannerTab(
    milestones: List<RoadmapMilestone>,
    checkedMilestones: SnapshotStateMap<String, Boolean>,
    onMilestoneToggle: (String, Boolean) -> Unit,
    missingSkillsCount: Int
) {
    // HORIZON INTENSITY ROADMAP STUDY SLIDER PLANNER
    var weekIntensitySliderValue by remember { mutableStateOf(10f) } // default 10 hours/week

    val totalHoursNeeded = remember(missingSkillsCount) {
        (missingSkillsCount * 12).coerceAtLeast(30) // Estimate 12 hours of specialized training per major technical gap
    }

    val weeksRequiredProjected = remember(totalHoursNeeded, weekIntensitySliderValue) {
        kotlin.math.ceil(totalHoursNeeded.toFloat() / weekIntensitySliderValue.coerceAtLeast(1f)).toInt().coerceIn(1, 26)
    }

    Column(
        verticalArrangement = Arrangement.spacedBy(16.dp),
        modifier = Modifier.fillMaxWidth()
    ) {
        // Study Intensity Slider Card
        Card(
            modifier = Modifier.fillMaxWidth(),
            border = BorderStroke(1.dp, MaterialTheme.colorScheme.onSurface.copy(alpha = 0.05f)),
            colors = CardDefaults.cardColors(containerColor = MaterialTheme.colorScheme.surface)
        ) {
            Column(modifier = Modifier.padding(14.dp)) {
                Row(
                    modifier = Modifier.fillMaxWidth(),
                    horizontalArrangement = Arrangement.SpaceBetween,
                    verticalAlignment = Alignment.CenterVertically
                ) {
                    Column {
                        Text(
                            text = "Interactive Weekly Study Speed Planner",
                            style = MaterialTheme.typography.bodyMedium,
                            fontWeight = FontWeight.Bold
                        )
                        Text(
                            text = "Tweak weekly dedication to visualize project completion milestones.",
                            style = MaterialTheme.typography.labelSmall,
                            color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.5f)
                        )
                    }
                    Box(
                        modifier = Modifier
                            .clip(RoundedCornerShape(6.dp))
                            .background(MaterialTheme.colorScheme.primary.copy(alpha = 0.1f))
                            .padding(horizontal = 8.dp, vertical = 4.dp)
                    ) {
                        Text(
                            text = "${weekIntensitySliderValue.toInt()} Hours/wk",
                            style = MaterialTheme.typography.labelMedium,
                            fontWeight = FontWeight.Black,
                            color = MaterialTheme.colorScheme.primary
                        )
                    }
                }

                Spacer(modifier = Modifier.height(10.dp))

                Slider(
                    value = weekIntensitySliderValue,
                    onValueChange = { weekIntensitySliderValue = it },
                    valueRange = 2f..40f,
                    steps = 19,
