/**
 * Robust structural parser utility for freeform text generated by Gemini to ensure premium segmented visual cards.
 */
object RoadmapParser {
    fun parseMilestones(rawText: String): List<RoadmapMilestone> {
        val result = mutableListOf<RoadmapMilestone>()
        val lines = rawText.split("\n")
            .map { it.trim() }
            .filter { it.isNotEmpty() && !it.startsWith("[") && !it.startsWith("]") }

        var currentTitle = ""
        val detailBuilder = StringBuilder()

        for (line in lines) {
            val cleanLine = line
                .replace(Regex("^[-*•\\d+.]\\s*"), "") // strip lead bullet symbols
                .replace(Regex("[*#_]"), "")           // strip markdown emphasis symbols
                .trim()

            if (cleanLine.lowercase().startsWith("phase") || 
                cleanLine.lowercase().startsWith("step") || 
                cleanLine.lowercase().startsWith("week") || 
                cleanLine.lowercase().startsWith("milestone") || 
                (cleanLine.length < 50 && cleanLine.contains(Regex("^\\s*\\d+")))) {
                
                if (currentTitle.isNotBlank()) {
                    result.add(
                        RoadmapMilestone(
                            id = "milestone_${result.size}_${currentTitle.hashCode()}",
                            title = currentTitle,
                            description = detailBuilder.toString().trim()
                        )
                    )
                    detailBuilder.clear()
                }
                currentTitle = cleanLine
            } else {
                if (currentTitle.isBlank() && cleanLine.length > 5) {
                    currentTitle = "Milestone ${result.size + 1}: Goal Setting"
                }
                detailBuilder.append(cleanLine).append(" ")
            }
        }

        if (currentTitle.isNotBlank()) {
            result.add(
                RoadmapMilestone(
                    id = "milestone_${result.size}_${currentTitle.hashCode()}",
                    title = currentTitle,
                    description = detailBuilder.toString().trim()
                )
            )
        }

        // Fallback for empty parsing to ensure high fidelity
        if (result.isEmpty()) {
            val paragraphs = rawText.split("\n\n").filter { it.isNotBlank() }
            paragraphs.forEachIndexed { i, para ->
                val clean = para.replace(Regex("[*#_]"), "").trim()
                val head = if (clean.length > 40) clean.take(35) + "..." else clean
                result.add(
                    RoadmapMilestone(
                        id = "milestone_fallback_$i",
                        title = "Learning Phase ${i + 1}: $head",
                        description = clean
                    )
                )
            }
        }

        return result
    }

    fun parseCertificates(rawText: String): List<CertificateGoal> {
        val list = mutableListOf<CertificateGoal>()
        val lines = rawText.split("\n")
            .map { it.replace(Regex("[*#_]"), "").trim() }
            .filter { it.isNotBlank() && !it.startsWith("[") && !it.startsWith("]") }

        lines.forEachIndexed { idx, line ->
            val clean = line.replace(Regex("^[-*•\\d+.]\\s*"), "").trim()
            if (clean.length > 5) {
                val halves = clean.split(Regex("[-:]"), 2)
                val title = halves.firstOrNull()?.trim() ?: "Credential Program ${idx + 1}"
                val desc = halves.getOrNull(1)?.trim() ?: "Pre-arranged certified track mapping to industry roles."
                
                // Extract provider from title
                val provider = if (title.lowercase().contains("google")) "Google Cloud"
                              else if (title.lowercase().contains("aws")) "Amazon Web Services"
                              else if (title.lowercase().contains("microsoft") || title.lowercase().contains("azure")) "Microsoft"
                              else if (title.lowercase().contains("comptia")) "CompTIA"
                              else if (title.lowercase().contains("cisco")) "Cisco Systems"
                              else "Professional Authority"

                list.add(CertificateGoal(
                    id = "cert_${idx}_${title.hashCode()}",
                    title = title,
                    provider = provider,
                    description = desc
                ))
            }
        }

        if (list.isEmpty()) {
            list.add(CertificateGoal("cert_fb_1", "AWS Certified Solutions Architect", "AWS", "Validates technical expertise in designing and deploying scalable systems."))
            list.add(CertificateGoal("cert_fb_2", "Google Associate Cloud Engineer", "Google Cloud", "Demonstrates your capability to plan, configure, and secure system resources."))
        }
        return list.take(4)
    }

    fun parseProjects(rawText: String): List<CapstoneProject> {
        val list = mutableListOf<CapstoneProject>()
        val blocks = rawText.split(Regex("\n(?=\\s*\\d+\\.)|\n(?=\\s*[-*•]\\s*[A-Z])|\n\n"))
            .map { it.replace(Regex("[#*_]"), "").trim() }
            .filter { it.isNotBlank() && !it.startsWith("[") && !it.startsWith("]") }

        blocks.forEachIndexed { idx, block ->
            val lines = block.split("\n").filter { it.isNotBlank() }
            val mainLine = lines.firstOrNull()?.replace(Regex("^[-*•\\d+.]\\s*"), "")?.trim() ?: ""
            if (mainLine.length > 5) {
                val halves = mainLine.split(Regex("[-:]"), 2)
                val title = halves.firstOrNull()?.trim() ?: "Capstone Build Project ${idx + 1}"
                val overview = halves.getOrNull(1)?.trim() ?: lines.drop(1).joinToString(" ").trim()
                
                val dynamicTasks = listOf(
                    "Setup project architecture and scaffolding schema",
                    "Design database structure and localized ORM tables",
                    "Integrate API services and responsive state controller loops",
                    "Configure security checkpoints, validation logic, and unit tests",
                    "Construct interactive Jetpack Compose user frontend dashboard",
                    "Host code repositories and release live build artifact demonstration"
                )

                list.add(CapstoneProject(
                    id = "project_${idx}_${title.hashCode()}",
                    title = title,
                    overview = if (overview.isBlank()) "Complete software system designed to validate core skills relevant to recruiters." else overview,
                    defaultTasks = dynamicTasks
                ))
            }
        }

        if (list.isEmpty()) {
            list.add(CapstoneProject("proj_fb_1", "High-Throughput Analytics microservice", "Event-driven architecture streaming database optimization logs.", listOf("Schema Setup", "Ingest Pipelines", "Visual Dashboard")))
        }
        return list.take(3)
    }
}

@Composable
fun InteractiveRoadmapDashboard(
    profile: ResumeProfile,
    modifier: Modifier = Modifier
) {
    val context = LocalContext.current
    var activeSubTab by remember { mutableStateOf(0) } // 0: Timeline Tracker, 1: Skill Gap, 2: Credentials & Capstones
    
    // Parsed assets state
    val milestones = remember(profile.learningRoadmap) { RoadmapParser.parseMilestones(profile.learningRoadmap) }
    val certificates = remember(profile.certificationSuggestions) { RoadmapParser.parseCertificates(profile.certificationSuggestions) }
    val projects = remember(profile.projectSuggestions) { RoadmapParser.parseProjects(profile.projectSuggestions) }

    // local preferences persistent trackers
    val checkPrefs = remember(profile.id) { context.getSharedPreferences("roadmap_checks_${profile.id}", Context.MODE_PRIVATE) }
    
    // Milestones Checked Map
    val checkedMilestones = remember { mutableStateMapOf<String, Boolean>() }
    // Project Task checked state: key = project_id + "_" + task_index
    val checkedProjectTasks = remember { mutableStateMapOf<String, Boolean>() }
    // Interactive Skill toggle state (Self-Educating vs Missing)
    val selfEducatedSkills = remember { mutableStateMapOf<String, Boolean>() }

    // Hydrate persistent state on start
    LaunchedEffect(profile.id) {
        milestones.forEach { m ->
            checkedMilestones[m.id] = checkPrefs.getBoolean("milestone_${m.id}", false)
        }
        projects.forEach { p ->
            p.defaultTasks.forEachIndexed { idx, _ ->
                val k = "task_${p.id}_$idx"
                checkedProjectTasks[k] = checkPrefs.getBoolean(k, false)
            }
        }
        val customSkillsJson = checkPrefs.getString("self_educated_skills", "") ?: ""
        if (customSkillsJson.isNotEmpty()) {
            customSkillsJson.split(",").filter { it.isNotBlank() }.forEach {
                selfEducatedSkills[it] = true
            }
        }
    }

    val missingSkillsList = remember(profile.missingSkills) {
        profile.missingSkills.split(",").map { it.trim() }.filter { it.isNotEmpty() }
    }
    val acquiredSkillsList = remember(profile.parsedSkills) {
        profile.parsedSkills.split(",").map { it.trim() }.filter { it.isNotEmpty() }
    }

    // Dynamic Progress Calculations (Highly Interactive!)
    val completedMilestoneCount = checkedMilestones.values.count { it }
    val totalMilestones = milestones.size.coerceAtLeast(1)
    val milestoneProgressFrac = completedMilestoneCount.toFloat() / totalMilestones.toFloat()

    val totalProjectTasks = projects.flatMap { p -> p.defaultTasks.mapIndexed { idx, _ -> "task_${p.id}_$idx" } }
    val completedProjectTasks = totalProjectTasks.count { checkedProjectTasks[it] == true }
    val projectProgressFrac = if (totalProjectTasks.isNotEmpty()) completedProjectTasks.toFloat() / totalProjectTasks.size.toFloat() else 0f

    val educationalProgressPct = remember(milestoneProgressFrac, projectProgressFrac) {
        val avg = (milestoneProgressFrac * 0.7f) + (projectProgressFrac * 0.3f)
        (avg * 100f).coerceIn(0f, 100f)
    }

    // Render Container
    Column(
        modifier = modifier
            .fillMaxWidth()
            .testTag("interactive_career_dashboard")
    ) {
        // TOP GENERAL STATS PANEL: Mastery Index Dial & General Projections
        MasteryStatsHeaderPanel(
            profile = profile,
            educationalProgressPct = educationalProgressPct,
            missingSkillsSize = missingSkillsList.size,
            selfEducatedCount = selfEducatedSkills.size,
            acquiredSkillsSize = acquiredSkillsList.size
        )

        Spacer(modifier = Modifier.height(14.dp))

        // SEGMENTED SUB-TABS ROW FOR EXPANSION
        NavigationBarSubtabs(activeSubTab = activeSubTab, onTabSelect = { activeSubTab = it })

        Spacer(modifier = Modifier.height(14.dp))

        // ANIMATED TRANSITION BOARD CONTENT
        AnimatedContent(
            targetState = activeSubTab,
            transitionSpec = {
                slideInHorizontally { width -> if (targetState > initialState) width else -width } + fadeIn() togetherWith
                slideOutHorizontally { width -> if (targetState > initialState) -width else width } + fadeOut()
            },
            label = "tab_navigation"
        ) { tabIndex ->
            when (tabIndex) {
                0 -> {
                    // TAB 0: INTERACTIVE PROGRESS TIMELINE & STUDIES INTENSITY PLANNER
                    TimelineAndPlannerTab(
                        milestones = milestones,
                        checkedMilestones = checkedMilestones,
                        onMilestoneToggle = { id, checked ->
                            checkedMilestones[id] = checked
                            checkPrefs.edit().putBoolean("milestone_$id", checked).apply()
                        },
                        missingSkillsCount = missingSkillsList.size
                    )
                }
                1 -> {
                    // TAB 1: DYNAMIC SKILL GAP MATRIX (CLICK-TO-LEARN INTERACTIVE CLOUD)
                    SkillGapMatrixTab(
                        acquiredSkills = acquiredSkillsList,
                        missingSkills = missingSkillsList,
                        selfEducatedSkills = selfEducatedSkills,
                        onToggleSelfEducated = { skill ->
                            val current = selfEducatedSkills[skill] ?: false
                            val next = !current
                            if (next) {
                                selfEducatedSkills[skill] = true
                            } else {
                                selfEducatedSkills.remove(skill)
                            }
                            val updatedList = selfEducatedSkills.keys.joinToString(",")
                            checkPrefs.edit().putString("self_educated_skills", updatedList).apply()
                        }
                    )
                }
                2 -> {
                    // TAB 2: PORTFOLIOS, PROJECTS & VERIFIED CERTIFICATIONS TRACKER
                    PortfoliosAndCertificationsTab(
                        certificates = certificates,
                        projects = projects,
                        checkedTasks = checkedProjectTasks,
                        onTaskToggle = { idKey, checked ->
                            checkedProjectTasks[idKey] = checked
                            checkPrefs.edit().putBoolean(idKey, checked).apply()
                        }
                    )
                }
            }
        }
    }
}

